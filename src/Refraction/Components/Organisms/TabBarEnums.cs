/// <summary>
/// Specifies the size variant for the TabBar.
/// </summary>
public enum TabBarSize
{
    /// <summary>
    /// Small tab bar size.
    /// </summary>
    Small,

    /// <summary>
    /// Default medium tab bar size.
    /// </summary>
    Medium,

    /// <summary>
    /// Large tab bar size.
    /// </summary>
    Large,
}

