namespace Refraction.Components.Organisms;

/// <summary>
///     Defines the visual variant of a lens container.
/// </summary>
public enum LensVariant
{
    /// <summary>
    ///     Default flat surface with subtle styling.
    /// </summary>
    Default,

    /// <summary>
    ///     Elevated surface with shadow, appears raised.
    /// </summary>
    Elevated,

    /// <summary>
    ///     Sunken surface, appears recessed.
    /// </summary>
    Sunken,

    /// <summary>
    ///     Card-style surface for content containers.
    /// </summary>
    Card,

    /// <summary>
    ///     Modal-style surface with highest elevation.
    /// </summary>
    Modal,
}