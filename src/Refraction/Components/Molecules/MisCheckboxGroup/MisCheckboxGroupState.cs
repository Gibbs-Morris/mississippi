namespace Mississippi.Refraction.Components.Molecules;

/// <summary>
///     Defines the visual states for <see cref="MisCheckboxGroup" />.
/// </summary>
public enum MisCheckboxGroupState
{
    /// <summary>
    ///     Default appearance.
    /// </summary>
    Default,

    /// <summary>
    ///     Indicates the group has an error.
    /// </summary>
    Error,

    /// <summary>
    ///     Indicates the group has a warning.
    /// </summary>
    Warning,

    /// <summary>
    ///     Indicates a successful or valid state.
    /// </summary>
    Success,
}
