/// <summary>
///     Generates client-side saga reducers for sagas marked with [GenerateSagaEndpoints].
/// </summary>
[Generator(LanguageNames.CSharp)]
public sealed class SagaClientReducersGenerator : IIncrementalGenerator
{
    private const string GenerateSagaEndpointsAttributeFullName =
        "Mississippi.Inlet.Generators.Abstractions.GenerateSagaEndpointsAttribute";

    private const string GeneratorName = "SagaClientReducersGenerator";

    private const string ISagaDefinitionFullName = "Mississippi.EventSourcing.Sagas.Abstractions.ISagaDefinition";

    /// <summary>
    ///     Recursively finds sagas in a namespace.
    /// </summary>
    private static void FindSagasInNamespace(
        INamespaceSymbol namespaceSymbol,
        INamedTypeSymbol sagaAttrSymbol,
        INamedTypeSymbol sagaDefinitionSymbol,
        string targetRootNamespace,
        List<SagaReducerInfo> sagas
    )
    {
        foreach (INamedTypeSymbol typeSymbol in namespaceSymbol.GetTypeMembers())
        {
            SagaReducerInfo? info = TryGetSagaReducerInfo(
                typeSymbol,
                sagaAttrSymbol,
                sagaDefinitionSymbol,
                targetRootNamespace);
            if (info is not null)
            {
                sagas.Add(info);
            }
        }

        foreach (INamespaceSymbol childNs in namespaceSymbol.GetNamespaceMembers())
        {
            FindSagasInNamespace(childNs, sagaAttrSymbol, sagaDefinitionSymbol, targetRootNamespace, sagas);
        }
    }

    /// <summary>
    ///     Generates the executing reducer.
    /// </summary>
    private static void GenerateExecutingReducer(
        SourceProductionContext context,
        SagaReducerInfo saga
    )
    {
        string reducerName = $"Start{saga.SagaName}SagaExecutingReducer";
        string actionName = $"Start{saga.SagaName}SagaExecutingAction";
        string stateName = $"{saga.SagaName}SagaFeatureState";
        SourceBuilder sb = new();
        sb.AppendAutoGeneratedHeader();
        sb.AppendUsing("Mississippi.Reservoir.Abstractions");
        sb.AppendUsing("Mississippi.Reservoir.Abstractions.Actions");
        sb.AppendUsing(saga.ActionsNamespace);
        sb.AppendUsing(saga.StateNamespace);
        sb.AppendFileScopedNamespace(saga.ReducersNamespace);
        sb.AppendLine();
        sb.AppendSummary($"Reducer for {actionName}.");
        sb.AppendGeneratedCodeAttribute(GeneratorName);
        sb.AppendLine($"internal sealed class {reducerName} : ActionReducerBase<{actionName}, {stateName}>");
        sb.OpenBrace();
        sb.AppendLine("/// <inheritdoc />");
        sb.AppendLine($"public override {stateName} Reduce(");
        sb.IncreaseIndent();
        sb.AppendLine($"{stateName} state,");
        sb.AppendLine($"{actionName} action");
        sb.DecreaseIndent();
        sb.AppendLine(")");
        sb.OpenBrace();
        sb.AppendLine("return state with");
        sb.OpenBrace();
        sb.AppendLine("IsExecuting = true,");
        sb.AppendLine("ExecutingSagaId = action.SagaId,");
        sb.AppendLine("ErrorMessage = null,");
        sb.AppendLine("ErrorCode = null");
        sb.CloseBrace();
        sb.AppendLine(";");
        sb.CloseBrace();
        sb.CloseBrace();
        context.AddSource($"{reducerName}.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
    }

