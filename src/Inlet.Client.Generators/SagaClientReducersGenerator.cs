    /// <summary>
    ///     Generates all reducers for a saga.
    /// </summary>
    private static void GenerateReducers(
        SourceProductionContext context,
        SagaReducerInfo saga
    )
    {
        GenerateExecutingReducer(context, saga);
        GenerateSucceededReducer(context, saga);
        GenerateFailedReducer(context, saga);
    }

